
public class WrapperClasses {

	public static void main(String[] args) {
		int a= 25;// Primitive data type
		Integer iw = new Integer(a);// non Primitive (Wrapper classes);
		System.out.println(iw);
		
		byte b= 29; // Primitive data type
		Byte bw = new Byte(b); // non Primitive (Wrapper classes)
		System.out.println(bw);
		
		short c= 30; // Primitive data type
		Short sw = new Short(c); // non Primitive (Wrapper classes)
		System.out.println(sw);

		
	}

}
